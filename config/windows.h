#define WIN_TILDE &kp KP_N1 &kp KP_N2 &kp KP_N6 // Alt+126 (~)
#define WIN_BKTK &kp KP_N9 &kp KP_N6            // Alt+96 (`)

#define WIN_LBKT &kp KP_N9 &kp KP_N1 // Alt+91 ([)
#define WIN_RBKT &kp KP_N9 &kp KP_N3 // Alt+93 (])

#define WIN_LBRC &kp KP_N1 &kp KP_N2 &kp KP_N3 // Alt+123 ({)
#define WIN_RBRC &kp KP_N1 &kp KP_N2 &kp KP_N5 // Alt+125 (})
